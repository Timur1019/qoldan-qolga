const LANG_KEY = 'lang'

export function getStoredLang() {
  try {
    const stored = localStorage.getItem(LANG_KEY)
    if (stored === 'ru' || stored === 'uz') return stored
  } catch (_) {}
  return 'uz'
}

export function setStoredLang(lang) {
  try {
    localStorage.setItem(LANG_KEY, lang)
  } catch (_) {}
}
